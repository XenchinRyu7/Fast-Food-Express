    public void authUserRegister() {
        if (name.isEmpty() || username.isEmpty() || pass.isEmpty() || confirmPass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Harap isi semua kolom.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            if (pass.equals(confirmPass)) {
                try {
                   st = initDatabase.connect.createStatement();
                   st.execute(querySql);
                   JOptionPane.showMessageDialog(null, "Register Berhasil", "Register Message", JOptionPane.INFORMATION_MESSAGE);
                   initDatabase.closeConnection();
                } catch (Exception e) {
                    System.out.println("" + e.getLocalizedMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password tidak sesuai dengan konfirmasi password.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
        }
    }
    
    public void authRegister() {
        name = jTextField1.getText();
        username = jTextField2.getText();
        pass = jTextField3.getText();
        confirmPass = jTextField4.getText();
        level = (String) jComboBox1.getSelectedItem();
        
        if (level.equals("Pembeli")) {
            
            querySql = "INSERT INTO buyer (buyer_name, buyer_username, buyer_password) " +
                   "VALUES ('" + name + "', '" + username + "', '" + pass + "')";
            authUserRegister();
            
        } else if (level.equals("Kasir")) {
            
            querySql = "INSERT INTO cashier (cashier_name, cashier_username, cashier_password) " +
                   "VALUES ('" + name + "', '" + username + "', '" + pass + "')";
            authUserRegister();
        } else {
            
            querySql = "INSERT INTO admin (admin_name, admin_username, admin_password) " +
                   "VALUES ('" + name + "', '" + username + "', '" + pass + "')";
            authUserRegister();
            
        }
    }

