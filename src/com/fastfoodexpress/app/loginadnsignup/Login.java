    public void authUser() {
        System.out.println(user + " " + Arrays.toString(pass));

        if (user.isEmpty() || pass.length == 0) {
            JOptionPane.showMessageDialog(null, "Username dan Password harus diisi", "Login Message", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Menampilkan pesan sukses
            JOptionPane.showMessageDialog(null, "Login Sukses sebagai " + user, "Login Message", JOptionPane.INFORMATION_MESSAGE);

            // Menutup koneksi
            initDatabase.closeConnection();

            // Menghapus password dari array
            Arrays.fill(pass, '0');

            // Menampilkan halaman utama atau melakukan tindakan lainnya setelah login berhasil
            // Misalnya, membuka MainForm
            UserForm mainForm = new UserForm();
            mainForm.setVisible(true);
            mainForm.pack();
            mainForm.setLocationRelativeTo(null);

            // Menutup halaman login
            this.dispose();
        }
    }

    private void authLogin() {
        user = jTextField1.getText();
        pass = jPasswordField1.getPassword();
        level = (String) jComboBox1.getSelectedItem();

        try {
            String tableName;
            String emailColumnName;
            String passwordColumnName;

            if (level.equals("Pembeli")) {
                tableName = "buyer";
                emailColumnName = "buyer_email";
                passwordColumnName = "buyer_password";
            } else if (level.equals("Kasir")) {
                tableName = "cashier";
                emailColumnName = "cashier_email";
                passwordColumnName = "cashier_password";
            } else {
                tableName = "admin";
                emailColumnName = "admin_email";
                passwordColumnName = "admin_password";
            }

            String querySql = "SELECT " + emailColumnName + ", " + passwordColumnName + " FROM " + tableName;
            PreparedStatement preparedStatement = initDatabase.connect.prepareStatement(querySql);

            rs = preparedStatement.executeQuery();

            while (rs.next()) {
                String userEmail = rs.getString(emailColumnName);
                String userPassword = rs.getString(passwordColumnName);

                // Membandingkan hasil query dengan input user
                if (user.equals(userEmail) && Arrays.equals(pass, userPassword.toCharArray())) {
                    authUser();
                    return;
                }
            }

            // Jika tidak ada hasil yang cocok
            JOptionPane.showMessageDialog(null, "Username dan Password salah", "Login Message", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

