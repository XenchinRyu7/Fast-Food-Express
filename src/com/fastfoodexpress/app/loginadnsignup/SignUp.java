    public void authUserRegister() {
        if (name.isEmpty() || email.isEmpty() || pass.length == 0 || confirmPass.length == 0) {
            JOptionPane.showMessageDialog(null, "Harap isi semua kolom.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            if (Arrays.equals(pass, confirmPass)) {
                try {
                    // Gunakan PreparedStatement untuk menghindari SQL injection
                    try (PreparedStatement pstmt = initDatabase.connect.prepareStatement(querySql)) {
                        pstmt.setString(1, name);
                        pstmt.setString(2, email);
                        pstmt.setString(3, new String(pass));

                        pstmt.executeUpdate();
                    }

                    JOptionPane.showMessageDialog(null, "Register Berhasil", "Register Message", JOptionPane.INFORMATION_MESSAGE);
                    initDatabase.closeConnection();
                } catch (SQLException e) {
                    System.out.println("SQL Error: " + e.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Password tidak sesuai dengan konfirmasi password.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
        }
    }

    public void authRegister() {
        name = jTextField1.getText();
        email = jTextField2.getText();
        pass = jPasswordField1.getPassword();
        confirmPass = jPasswordField2.getPassword();
        level = (String) jComboBox1.getSelectedItem();

        if (level.equals("Pembeli")) {
            querySql = "INSERT INTO buyer (buyer_name, buyer_email, buyer_password) VALUES (?, ?, ?)";
            authUserRegister();
        } else if (level.equals("Kasir")) {
            querySql = "INSERT INTO cashier (cashier_name, cashier_email, cashier_password) VALUES (?, ?, ?)";
            authUserRegister();
        } else {
            querySql = "INSERT INTO admin (admin_name, admin_email, admin_password) VALUES (?, ?, ?)";
            authUserRegister();
        }
    }

